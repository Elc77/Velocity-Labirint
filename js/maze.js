window.onload = function () {
    const canvas = document.getElementById('mazeCanvas');
    const ctx = canvas.getContext('2d');
    SVGIcons["30 by 30 orthogonal maze.svg"].draw(ctx);

    const startButton = document.querySelector('.menu_buttons input[value="Start"]');
    const resetButton = document.querySelector('.menu_buttons input[value="Reset"]');
    const speedSlider = document.getElementById("speedSlider");
    const speedValue = document.getElementById("speedValue");
    const lightDiv = document.querySelector('.light'); // Light effect div
    const lightBulb = document.querySelector('.menu_buttons img'); // Lightbulb image

    //  Hide yellow light effect at start
    lightDiv.style.display = "none";

    startButton.addEventListener('click', startAnimation);
    resetButton.addEventListener('click', resetAnimation);
    speedSlider.addEventListener("input", () => {
        speedValue.textContent = speedSlider.value; // Update speed display
        updateLightEffect(speedSlider.value); // Adjust light effect based on speed
    });

    function updateLightEffect(speed) {
        let intensity = speed / 15;
        let minGlow = 3;
        let maxGlow = 50;
        let glowSize = minGlow + (maxGlow - minGlow) * (intensity * intensity);

        lightDiv.style.backgroundColor = `rgba(255, 255, 0, ${0.2 + intensity * 0.6})`;
        lightDiv.style.boxShadow = `0 0 ${glowSize}px rgba(255, 255, 0, ${0.3 + intensity * 0.7})`;
    }
};

